package edu.bonn.jbc.exceptions.task4;

public class MainClass {
	
	public static void main(String[] args) {
		Pizza pizza = new Pizza("Margherita");
		printSize(pizza);
	}
	
	private static void printSize(Pizza pizza) {
		if (pizza.getSize().equals("S")) {
			System.out.println("The pizza is small");
		} else if (pizza.getSize().equals("M")) {
			System.out.println("The pizza is medium");
		} else if (pizza.getSize().equals("L")) {
			System.out.println("The pizza is large");
		} else {
			System.out.println("We don't know about the pizza's size :(");
		}
	}

}
